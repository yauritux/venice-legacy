package com.gdn.venice.exportimport.logistics.dataexport;

public class ActivityInvoiceReconRecord {
	private String airwayBillId;
	private String reconRecordResult;
	private String veniceData;
	private String providerData;
	private String manualData;
	private String pic;
	private String actionApplied;
	private long reconResultId;
	
	
	public String getAirwayBillId() {
		return airwayBillId;
	}
	public void setAirwayBillId(String airwayBillId) {
		this.airwayBillId = airwayBillId;
	}
	public String getReconRecordResult() {
		return reconRecordResult;
	}
	public void setReconRecordResult(String reconRecordResult) {
		this.reconRecordResult = reconRecordResult;
	}
	public String getVeniceData() {
		return veniceData;
	}
	public void setVeniceData(String veniceData) {
		this.veniceData = veniceData;
	}
	public String getProviderData() {
		return providerData;
	}
	public void setProviderData(String providerData) {
		this.providerData = providerData;
	}
	public String getManualData() {
		return manualData;
	}
	public void setManualData(String manualData) {
		this.manualData = manualData;
	}
	public String getPic() {
		return pic;
	}
	public void setPic(String pic) {
		this.pic = pic;
	}
	public String getActionApplied() {
		return actionApplied;
	}
	public void setActionApplied(String actionApplied) {
		this.actionApplied = actionApplied;
	}
	public long getReconResultId() {
		return reconResultId;
	}
	public void setReconResultId(long reconResultId) {
		this.reconResultId = reconResultId;
	}
}
